  static CharacterStats generateStats(
      String rarity, CharacterType type, CharacterRace race) {
    final base = _baseStats[type] ?? _baseStats[CharacterType.unknown]!;
    final raceMod =
        _raceModifiers[race] ?? _raceModifiers[CharacterRace.unknown]!;

    int rarityBonus = switch (rarity) {
      'Common' => _random.nextInt(5) + 1,
      'Rare' => _random.nextInt(6) + 5,
      'Unique' => _random.nextInt(6) + 10,
      _ => 0,
    };

    int applyModifier(int baseValue, String stat) =>
        ((baseValue * raceMod[stat]!) + rarityBonus).clamp(1, 9999).toInt();

    return CharacterStats(
      hp: applyModifier(base['hp']!, 'hp'),
      mp: applyModifier(base['mp']!, 'mp'),
      sp: applyModifier(base['sp']!, 'sp'),
      attackPoints: applyModifier(base['atk']!, 'atk'),
      defensePoints: applyModifier(base['def']!, 'def'),
      agilityPoints: applyModifier(base['agi']!, 'agi'),
      criticalPoints: applyModifier(base['crit']!, 'crit'),
    );
  }
}

GirlFarmer createGirlFarmer({
  required String id,
  required String name,
  required String rarity,
  required int stars,
  required double miningEfficiency,
  required String image,
  required String imageFace,
  required CharacterRace race,
  required CharacterType type,
  required String region,
  required String description,
}) {
  final stats = StatGenerator.generateStats(rarity, type, race);
  final statsMap = stats.toMap();

  return GirlFarmer(
    id: id,
    name: name,
    rarity: rarity,
    stars: stars,
    miningEfficiency: miningEfficiency,
    image: image,
    imageFace: imageFace,
    race: race.name,
    type: type.name,
    region: region,
    description: description,
    criticalPoint: statsMap['criticalPoints']!,
    abilities: RaceAbilities.getStarterAbilities(race.name),
    hp: statsMap['hp']!,
    mp: statsMap['mp']!,
    sp: statsMap['sp']!,
    attackPoints: statsMap['attackPoints']!,
    defensePoints: statsMap['defensePoints']!,
    agilityPoints: statsMap['agilityPoints']!,
    maxHp: statsMap['maxHp']!,
    maxMp: statsMap['maxMp']!,
    maxSp: statsMap['maxSp']!,
  );
}

