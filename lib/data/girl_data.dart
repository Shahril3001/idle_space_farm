class CharacterStats {
  final int hp;
  final int mp;
  final int sp;
  final int attackPoints;
  final int defensePoints;
  final int agilityPoints;
  final int criticalPoints;

  CharacterStats({
    required this.hp,
    required this.mp,
    required this.sp,
    required this.attackPoints,
    required this.defensePoints,
    required this.agilityPoints,
    required this.criticalPoints,
  });

  Map<String, int> toMap() => {
        'hp': hp,
        'mp': mp,
        'sp': sp,
        'attackPoints': attackPoints,
        'defensePoints': defensePoints,
        'agilityPoints': agilityPoints,
        'criticalPoints': criticalPoints,
        'maxHp': hp,
        'maxMp': mp,
        'maxSp': sp,
      };
}

